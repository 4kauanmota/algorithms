//.fill (element, start, end)


let array = [1, 2, 3, 4];


array.fill(1);
console.log(array);
/*
 __________________
|  RESULT 1        |
|------------------|
| * [1, 1, 1, 1]   |
|__________________|

*/


array.fill(2, 1);
console.log(array);
/*
 __________________
|  RESULT 2        |
|------------------|
| * [1, 2, 2, 2]   |
|__________________|

*/


array.fill(3, 1, 3);
console.log(array);
/*
 __________________
|  RESULT 3        |
|------------------|
| * [1, 3, 3, 2]   |
|__________________|

*/


let array2 = new Array(5).fill(" ");
console.log(array2);
/*
 _______________________________
|  RESULT 4                     |
|-------------------------------|
| * [' ', ' ', ' ', ' ', ' ', ] |
|_______________________________|

*/


array2.fill('k', 2, 3)
console.log(array2);
/*
 _______________________________
|  RESULT 5                     |
|-------------------------------|
| * [' ', ' ', 'k', ' ', ' ', ] |
|_______________________________|

*/